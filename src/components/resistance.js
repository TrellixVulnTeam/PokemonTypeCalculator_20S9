class Resistance extends React.Component {
    
    constructor(props) {
        super(props);
    }

    findResistance(type1, type2){
        const result = [];
        for(var i = 0; i < chart.length; i++){
            if(chart[type1][i] * chart[type2][i] < 1){
                result.push(i)
            }
        }
        return result
    }

    render() {
        var results = this.findResistance(this.props.type1, this.props.type2)
        return (
            <td>
            {results.map((resistance, index) => (
                typeNames[resistance] + " "
            ))}
            </td>
            );
    }
};

